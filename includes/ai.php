# دقت علمی در فیزیک
## فشار و شاره‌ها
- فشار مایع: $P=\rho gh$.
- فشار کل ظرف باز: $P=P_0+\rho gh$.
- اختلاف فشار: $\Delta P=\rho g\Delta h$؛ فشار هوای مشترک حذف می‌شود.
- نیرو از فشار: $F=PA$.
- در لوله U: نقاط هم‌تراز در یک مایع ساکن و پیوسته هم‌فشارند.
- در چند مایع: فشار ستون‌ها را لایه‌به‌لایه جمع کن: $P=P_0+\rho_1gh_1+\rho_2gh_2+...$.
- در مانومتر: سطح پایین‌تر مایع معمولاً سمت فشار بیشتر است. اگر فشار گاز بیشتر باشد: $P_{gas}=P_0+\rho gh$؛ اگر کمتر باشد: $P_{gas}=P_0-\rho gh$.
- فشار پیمانه‌ای: $P_g=P_{gas}-P_0$.
- شناوری: $F_b=\rho_{fluid}gV_{sub}$؛ حجم قسمت فرو رفته/مایع جابه‌جا شده مهم است.
- وضعیت جسم: اگر $W>F_b$ ته‌نشین، اگر $W=F_b$ غوطه‌ور/تعادل، اگر $W<F_b$ شناور/بالارونده.

## شاره در حرکت
- آهنگ شارش: $Q=\frac{\Delta V}{\Delta t}=Av$.
- پیوستگی: $A_1v_1=A_2v_2$.
- اگر مقطع دایره‌ای است: $A\propto r^2$ و $A\propto d^2$.
- برنولی در مسیر افقی: سرعت بیشتر، فشار کمتر؛ سرعت کمتر، فشار بیشتر.

## کار، انرژی و توان
- کار نیروی ثابت: $W=Fd\cos\theta$.
- نیروی عمود بر جابه‌جایی کار انجام نمی‌دهد.
- اصطکاک: $W_f=-f_kd$ و $d$ طول مسیر است.
- کار وزن: $W_g=mg(h_i-h_f)$ و فقط به اختلاف ارتفاع وابسته است.
- انرژی پتانسیل گرانشی: $U=mgh$، تغییر آن: $\Delta U=mg(h_2-h_1)$.
- $W_g=-\Delta U$.
- انرژی جنبشی: $K=\frac12mv^2$؛ در نسبت‌ها سرعت مربع می‌شود.
- انرژی مکانیکی: $E=K+U$.
- بدون اتلاف: $K_1+U_1=K_2+U_2$.
- با اصطکاک/مقاومت: $W_{nc}=\Delta K+\Delta U=E_2-E_1$.
- فنر: $U_s=\frac12kx^2$.
- توان: $P=\frac{W}{t}$ و اگر نیرو و سرعت هم‌راستا باشند $P=Fv$.
- نمودار نیرو-مکان: کار برابر سطح علامت‌دار زیر نمودار است؛ زیر محور منفی است.

## گرما و دما
- گرمای مبادله‌شده: $Q=mc\Delta\theta$.
- تغییر حجم گرمایی: $\Delta V=\beta V_0\Delta T$.
- تعادل گرمایی: مجموع گرماهای مبادله‌شده صفر است: $Q_1+Q_2+...=0$.
- گرمای نهان: $Q=mL$.
- بازده: $\eta=\frac{P_{out}}{P_{in}}\times100$ یا $\eta=\frac{W_{useful}}{W_{input}}\times100$.

